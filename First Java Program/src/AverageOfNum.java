public class AverageOfNum {

    static void averageOfAll(int a, int b, int c, int d, int e) {
        int total = a + b + c + d + e;
        int average = total/5;
        System.out.println("Total of 5 number is : " + average);

    }

    public static void main(String[] args) {

        averageOfAll(10,5,6,90,25);




    }


}
